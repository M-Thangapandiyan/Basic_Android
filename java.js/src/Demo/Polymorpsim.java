package Demo;

public class Polymorpsim {

    public static int multiplay(int arg1, int arg2){
        return arg1*arg2;
    }

    public static int multiplay(int arg1, int arg2, int arg3){
        return arg1*arg2*arg3;
    }

    public int add (int arg1, int arg2){
        return arg1+arg2;
    }

    public String add(String arg1, String arg2){
        return arg1+arg2;
    }

}
